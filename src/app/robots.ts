import type { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: {
      userAgent: "*",
      allow: "/",
      disallow: ["/user/dashboard/", "/user/settings/", "/user/welcome/"],
    },
    sitemap: `${process.env.SERVER_URL}/sitemap.xml`,
  };
}
